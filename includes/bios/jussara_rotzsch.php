				<p>Jussara R&ouml;tzsch has a Masters in Psychiatry and today specialises in health informatics and e-health strategy. She is also a consultant to the Brazil Ministry of Health in the national EHR project. She works as Business Analyst of the exchange content model of TISS project (Nationwide Electonic Health Information Exchange between health plans and healthcare providers).</p>
				<p>Jussara became personality of the year in the category Incentivating E-business of Brasilian Association of E-business in 2006 and awarded at WoHIT'07 (World of Health IT Conference Award) in Vienna in 2007. Chair of National Committee for Health Information Standards and Interoperability within Private Health Insurance market (COPISS), from 2006 to 2009. Convener of WG 3- Semantic Content of HIT Commitee of Brazilian Standards Development Organization (ABNT) from 2007 to 2009.</p>
